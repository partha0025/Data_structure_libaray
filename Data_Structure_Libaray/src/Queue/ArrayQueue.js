class ArrayQueue {
    constructor() {
        this.queue = [];
    }

    enqueue(value) {
        this.queue.push(value);
    }

    dequeue() {
        return this.queue.shift();
    }

    peek() {
        return this.queue[0];
    }

    isEmpty() {
        return this.queue.length === 0;
    }

    print() {
        console.log(this.queue.join(" -> "));
    }
}

module.exports = ArrayQueue;
